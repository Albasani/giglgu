WinError::WinError()
: m_error(GetLastError())
{}

WinError::WinError(ErrorType error)
: m_error(error)
{}

void* WinError::formatMessage() const
{
  LPVOID lpMsgBuf = 0;
  DWORD rc = ::FormatMessage(
    FORMAT_MESSAGE_ALLOCATE_BUFFER |
    FORMAT_MESSAGE_FROM_SYSTEM |
    FORMAT_MESSAGE_IGNORE_INSERTS,
    0,
    m_error,
    MAKELANGID(LANG_NEUTRAL, SUBLANG_DEFAULT), // Default language
    (LPTSTR) &lpMsgBuf,
    0,
    0
  );
  return rc == 0 ? 0 : lpMsgBuf;
}

std::ostream& WinError::print(std::ostream& strm) const
{
  strm << '(';
  const char* name = GetErrorName(m_error);
  if (name != 0)
    strm << name << "=";
  strm << m_error << ')';

  void* lpMsgBuf = formatMessage();
  if (lpMsgBuf != 0)
  {
    strm << ' ' << static_cast<LPTSTR>(lpMsgBuf);
    LocalFree(lpMsgBuf);
  }
  
  return strm;
}

std::string WinError::getString() const
{
  void* lpMsgBuf = formatMessage();
  if (lpMsgBuf != 0)
  {
    std::string result = static_cast<LPTSTR>(lpMsgBuf);
    LocalFree(lpMsgBuf);
    return result;
  }
  else
  {
    char sz[0x20];
    _snprintf(sz, sizeof(sz), "%u", m_error);
    return sz;
  }
}

